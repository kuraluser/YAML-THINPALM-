  /**
* initialise tanks data
*
* @memberof UllageUpdatePopupComponent
*/
  async getShipLandingTanks() {
    this.prevQuantitySelectedUnit = AppConfigurationService.settings.baseUnit;
    this.convertQuantityToSelectedUnit();
  }

  /**
  * Convert quantity to selected uint
  *
  * @memberof UllageUpdatePopupComponent
  */
  convertQuantityToSelectedUnit() {
    this.rearBallastTanks = this.arrivalConditionTransformationService.formatBallastTanks(this.voyageStatusResponse?.ballastRearTanks, this.voyageStatusResponse?.ballastQuantities, this.prevQuantitySelectedUnit, this.currentQuantitySelectedUnit);
    this.centerBallastTanks = this.arrivalConditionTransformationService.formatBallastTanks(this.voyageStatusResponse?.ballastCenterTanks, this.voyageStatusResponse?.ballastQuantities, this.prevQuantitySelectedUnit, this.currentQuantitySelectedUnit);
    this.frontBallastTanks = this.arrivalConditionTransformationService.formatBallastTanks(this.voyageStatusResponse?.ballastFrontTanks, this.voyageStatusResponse?.ballastQuantities, this.prevQuantitySelectedUnit, this.currentQuantitySelectedUnit);
  }

